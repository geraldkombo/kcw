let _currentLang = "en";

export function setLanguage(lang) {
  if (TRANSLATIONS[lang]) _currentLang = lang;
}

export function t(key, ...args) {
  let msg = TRANSLATIONS[_currentLang]?.[key] ?? TRANSLATIONS.en[key] ?? key;
  if (args.length) msg = msg.replace("{n}", args[0]);
  return msg;
}

export function currentLang() {
  return _currentLang;
}
